package controle;

public class usandoFor {

	public static void main(String[] args) {
		//Laço com número conhecido de repetições
		
		for (int pikachu = 1; pikachu <= 2; pikachu++) {
			System.out.println("Pikachu use choque do trovão!!");
		}
		
		for (int squilter = 1; squilter <= 2; squilter ++) {
			System.out.println("Squilter use Hydrobomb!");
		}
	}
}
